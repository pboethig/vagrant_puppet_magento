/**
 * Cmspage comment list block
 *
 * @category    Ibrams
 * @package     Ibrams_CmsExtended
 * @author Ultimate Module Creator
 */
class Ibrams_CmsExtended_Block_Cmspage_Comment_List extends Mage_Core_Block_Template
{
    /**
     * initialize
     *
     * @access public
     * @author Ultimate Module Creator
     */
    public function __construct()
    {
        parent::__construct();
        $cmspage = $this->getCmspage();
        $comments = Mage::getResourceModel('ibrams_cmsextended/cmspage_comment_collection')
            ->addFieldToFilter('cmspage_id', $cmspage->getId())
            ->addStoreFilter(Mage::app()->getStore())
             ->addFieldToFilter('status', 1);
        $comments->setOrder('created_at', 'asc');
        $this->setComments($comments);
    }

    /**
     * prepare the layout
     *
     * @access protected
     * @return Ibrams_CmsExtended_Block_Cmspage_Comment_List
     * @author Ultimate Module Creator
     */
    protected function _prepareLayout()
    {
        parent::_prepareLayout();
        $pager = $this->getLayout()->createBlock(
            'page/html_pager',
            'ibrams_cmsextended.cmspage.html.pager'
        )
        ->setCollection($this->getComments());
        $this->setChild('pager', $pager);
        $this->getComments()->load();
        return $this;
    }

    /**
     * get the pager html
     *
     * @access public
     * @return string
     * @author Ultimate Module Creator
     */
    public function getPagerHtml()
    {
        return $this->getChildHtml('pager');
    }
    /**
     * get the current cmspage
     *
     * @access protected
     * @return Ibrams_CmsExtended_Model_Cmspage
     * @author Ultimate Module Creator
     */
    public function getCmspage()
    {
        return Mage::registry('current_cmspage');
    }
}
